function setLang(lang) {
  document.querySelectorAll('[data-i18n]').forEach(function(el){
    var key = el.getAttribute('data-i18n');
    if (T[key] && T[key][lang]) {
      var val = T[key][lang].replace(/<br>/g,'\n');
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el){
    var key = el.getAttribute('data-i18n-ph');
    if (T[key] && T[key][lang]) el.placeholder = T[key][lang];
  });
  if (document.getElementById('langSel')) {
    document.getElementById('langSel').value = lang;
  }
  document.documentElement.lang = lang;
  history.replaceState(null,'',location.pathname+'?lang='+lang);
}

function getSavedLang(){
  var params=new URLSearchParams(window.location.search);
  if(params.get('lang')) return params.get('lang');
  var saved=localStorage.getItem('lang');
  return saved||'en';
}

document.addEventListener('DOMContentLoaded',function(){
  var lang=getSavedLang();
  setLang(lang);
  if(document.getElementById('langSel')){
    document.getElementById('langSel').onchange=function(){
      var l=this.value;
      localStorage.setItem('lang',l);
      setLang(l);
    };
  }
  // Mobile menu
  var mm=document.getElementById('mobileMenu');
  var nav=document.querySelector('nav');
  if(mm&&nav){
    mm.onclick=function(){ nav.classList.toggle('open'); };
  }
});
